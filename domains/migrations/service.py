# domains/migrations/service.py

"""
Migrations service.

Key fixes applied (original)
-----------------------------
1. order_index race condition — SELECT FOR UPDATE lock before computing next index.
2. generated_by as typed enum / documented constant.
3. backup_id provenance field.
4. restore migrations are auto-applied.
5. Pre-storage SQL validation (dry-run via rolled-back asyncpg transaction).

New: Migration Conflict Resolution
------------------------------------
detect_conflicts() is now called automatically inside create_migration()
when a new migration is submitted. It checks whether the incoming SQL
would conflict with already-pending (unapplied) migrations targeting the
same objects.

check_divergent_heads() detects the case where two developers have both
created migrations against the same applied baseline — i.e. there is more
than one "tip" in the pending queue that cannot be linearised without
manual intervention.

resolve_conflict() provides a structured workflow for conflict resolution:
  - "rebase"  — reorder the conflicting migration to run after the blocker
  - "squash"  — merge two pending migrations into one combined record
  - "skip"    — mark a pending migration as skipped (won't be applied)

ConflictReport is returned from create_migration() in the meta field and
exposed via the router so the UI can display it inline.

Permission guard
----------------
create_migration() and apply_migration() now call assert_connection_writable()
which fetches the stored capabilities for the connection and raises
PermissionError (HTTP 403) if the connection role is read-only. This is
the single enforcement point — the router translates it to an HTTP response.
"""

