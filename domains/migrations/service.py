async def assert_connection_writable(
    db: AsyncSession,
    connection_id: UUID,
    operation: str = "This migration operation",
) -> None:
    """
    Fetch the connection's stored capabilities and raise PermissionError
    if the role is read-only.

    This is called by create_migration() and apply_migration() so that
    the error surfaces before any SQL is sent to the target database.
    """
    from domains.connections.models import Connection
    from domains.connections.service import assert_writable

    result = await db.execute(
        select(Connection.capabilities).where(
            Connection.id == connection_id,
            Connection.is_active == True,  # noqa: E712
        )
    )
    caps = result.scalar_one_or_none()
    if caps is None:
        # Connection not found — let downstream handle it
        return

    try:
        assert_writable(caps, operation)
    except ValueError as exc:
        raise PermissionError(str(exc)) from exc


