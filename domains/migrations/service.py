async def list_migrations(
    db: AsyncSession,
    connection_id: UUID,
    applied: bool | None = None,
    limit: int = 100,
    offset: int = 0,
    order_index_from: int | None = None,
    order_index_to: int | None = None,
) -> list[MigrationRecord]:
    q = select(MigrationRecord).where(
        MigrationRecord.connection_id == connection_id
    )
    if applied is not None:
        q = q.where(MigrationRecord.applied == applied)
    if order_index_from is not None:
        q = q.where(MigrationRecord.order_index >= order_index_from)
    if order_index_to is not None:
        q = q.where(MigrationRecord.order_index <= order_index_to)

    q = q.order_by(MigrationRecord.order_index.asc()).offset(offset).limit(limit)
    result = await db.execute(q)
    return list(result.scalars().all())


async def get_migration(
    db: AsyncSession,
    migration_id: UUID,
) -> MigrationRecord | None:
    result = await db.execute(
        select(MigrationRecord).where(MigrationRecord.id == migration_id)
    )
    return result.scalar_one_or_none()


async def get_migration_at_order_index(
    db: AsyncSession,
    connection_id: UUID,
    order_index: int,
) -> MigrationRecord | None:
    result = await db.execute(
        select(MigrationRecord).where(
            MigrationRecord.connection_id == connection_id,
            MigrationRecord.order_index == order_index,
        )
    )
    return result.scalar_one_or_none()


async def get_last_applied(
    db: AsyncSession,
    connection_id: UUID,
) -> MigrationRecord | None:
    result = await db.execute(
        select(MigrationRecord)
        .where(
            MigrationRecord.connection_id == connection_id,
            MigrationRecord.applied == True,  # noqa
        )
        .order_by(MigrationRecord.order_index.desc())
        .limit(1)
    )
    return result.scalar_one_or_none()


async def delete_migration(
    db: AsyncSession,
    migration_id: UUID,
) -> bool:
    record = await get_migration(db, migration_id)
    if not record:
        return False
    if record.applied:
        raise ValueError("Cannot delete an applied migration. Roll it back first.")
    await db.delete(record)
    await db.commit()
    return True


