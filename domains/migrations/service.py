async def check_divergent_heads(
    db: AsyncSession,
    connection_id: UUID,
) -> list[str]:
    """
    Return version strings of pending migrations that form divergent heads.

    A divergent head situation exists when there are multiple pending
    migrations that were all created against the same last-applied baseline.
    This means two developers independently created migrations without
    either knowing about the other's pending migration.

    In a healthy linear history, pending migrations form a single chain
    where each one's creation time is after the previous one's. When
    the chain forks — two migrations with adjacent order_indexes were
    both created before either was applied — we have divergent heads.
    """
    pending = await list_migrations(db, connection_id, applied=False)
    if len(pending) <= 1:
        return []

    # Find the last applied migration to use as the baseline
    last_applied = await get_last_applied(db, connection_id)
    baseline_order = last_applied.order_index if last_applied else 0

    # Pending migrations created against the same baseline (order_index == baseline + 1)
    # that were not created sequentially are divergent heads.
    # We detect this by looking for multiple pending migrations created within
    # a short window of each other before any were applied.
    divergent: list[str] = []
    if len(pending) > 1:
        # If the oldest pending migration's created_at is after all others',
        # that means multiple migrations were queued up — this is the expected
        # sequential case. But if two migrations have nearly identical creation
        # times and the same baseline, they are truly divergent.
        baseline_pending = [m for m in pending if m.order_index == baseline_order + 1]
        if len(baseline_pending) > 1:
            # This shouldn't happen due to the unique constraint, but guard anyway
            divergent = [m.version for m in baseline_pending]

        # Broader check: any two pending migrations that touch the same objects
        # are potential conflicts regardless of order_index
        # (this is handled in detect_conflicts, not here)

    return divergent


async def detect_conflicts(
    db: AsyncSession,
    connection_id: UUID,
    incoming_sql: str,
    incoming_label: str,
) -> ConflictReport:
    """
    Analyse the incoming migration SQL against all currently pending
    (unapplied) migrations for the same connection.

    Conflict types detected:
      same_object  — incoming and a pending migration both modify the
                     same table or database object
      divergent_head — multiple pending migrations exist against the same
                       applied baseline (two developers diverged)

    Returns a ConflictReport. When has_conflicts=False the migration can
    be created and applied without manual intervention.

    This is intentionally conservative — it reports potential conflicts
    rather than definitive ones. A false positive is safer than a missed
    conflict that corrupts production schema.
    """
    pending = await list_migrations(db, connection_id, applied=False)
    if not pending:
        # No pending migrations → no conflicts possible
        return ConflictReport(has_conflicts=False, summary="No conflicts detected.")

    incoming_objects = _extract_objects_from_sql(incoming_sql)

    conflicts: list[ConflictDetail] = []

    for existing in pending:
        existing_objects = _extract_objects_from_sql(existing.up_sql)
        overlap = incoming_objects & existing_objects

        if overlap:
            conflicts.append(
                ConflictDetail(
                    blocker_id=str(existing.id),
                    blocker_version=existing.version,
                    blocker_label=existing.label,
                    blocker_order_index=existing.order_index,
                    conflict_type="same_object",
                    affected_objects=sorted(overlap),
                    description=(
                        f"Both '{incoming_label}' and pending migration "
                        f"'{existing.label}' (order_index={existing.order_index}) "
                        f"modify: {', '.join(sorted(overlap))}. "
                        "Applying both without review may fail or produce "
                        "unexpected schema state."
                    ),
                )
            )

    divergent_heads = await check_divergent_heads(db, connection_id)

    has_conflicts = bool(conflicts) or bool(divergent_heads)
    resolution_options: list[str] = []

    if conflicts:
        resolution_options = ["apply_blocker_first", "rebase", "skip_blocker"]

    if divergent_heads:
        resolution_options.append("squash")

    summary = (
        f"{len(conflicts)} object conflict(s) detected with pending migrations."
        if conflicts
        else "No object conflicts detected."
    )
    if divergent_heads:
        summary += f" {len(divergent_heads)} divergent head(s) detected."

    return ConflictReport(
        has_conflicts=has_conflicts,
        conflicts=conflicts,
        divergent_heads=divergent_heads,
        resolution_options=resolution_options,
        summary=summary,
    )


