@dataclass
class ConflictDetail:
    """A single detected conflict between two pending migrations."""
    blocker_id: str            # UUID of the migration this conflicts with
    blocker_version: str
    blocker_label: str
    blocker_order_index: int
    conflict_type: str         # "same_table" | "same_object" | "divergent_head"
    affected_objects: list[str]  # Table/object names involved
    description: str


@dataclass
class ConflictReport:
    """
    Full conflict analysis for a migration being created.
    Stored in MigrationRecord.meta["conflict_report"] and returned
    from the router so the UI can display it inline.
    """
    has_conflicts: bool
    conflicts: list[ConflictDetail] = field(default_factory=list)
    divergent_heads: list[str] = field(default_factory=list)  # version strings
    resolution_options: list[str] = field(default_factory=list)
    summary: str = ""

    def to_dict(self) -> dict[str, Any]:
        return {
            "has_conflicts": self.has_conflicts,
            "conflicts": [
                {
                    "blocker_id": c.blocker_id,
                    "blocker_version": c.blocker_version,
                    "blocker_label": c.blocker_label,
                    "blocker_order_index": c.blocker_order_index,
                    "conflict_type": c.conflict_type,
                    "affected_objects": c.affected_objects,
                    "description": c.description,
                }
                for c in self.conflicts
            ],
            "divergent_heads": self.divergent_heads,
            "resolution_options": self.resolution_options,
            "summary": self.summary,
        }


# ---------------------------------------------------------------------------
# Version generator
# ---------------------------------------------------------------------------

def _generate_version() -> str:
    """e.g. 20240115_143022"""
    return datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")


def _checksum(sql: str) -> str:
    return hashlib.sha256(sql.strip().encode()).hexdigest()


# ---------------------------------------------------------------------------
# SQL object extraction (for conflict detection)
# ---------------------------------------------------------------------------

def _extract_objects_from_sql(sql: str) -> set[str]:
    """
    Extract table and object names from SQL for conflict analysis.
    Conservative — extracts identifiers after common DDL keywords.
    Returns a set of lowercased object names.
    """
    objects: set[str] = set()

    # Patterns: ALTER TABLE x, DROP TABLE x, CREATE TABLE x,
    #           CREATE INDEX ON x, DROP INDEX x
    patterns = [
        r'(?:ALTER|DROP|CREATE(?:\s+OR\s+REPLACE)?)\s+TABLE\s+(?:IF\s+(?:NOT\s+)?EXISTS\s+)?(?:"?(\w+)"?\.)?"?(\w+)"?',
        r'CREATE\s+(?:UNIQUE\s+)?INDEX\s+(?:\w+\s+)?ON\s+(?:"?(\w+)"?\.)?"?(\w+)"?',
        r'DROP\s+INDEX\s+(?:IF\s+EXISTS\s+)?(?:"?(\w+)"?\.)?"?(\w+)"?',
        r'(?:ALTER|DROP|CREATE)\s+TYPE\s+(?:IF\s+(?:NOT\s+)?EXISTS\s+)?(?:"?(\w+)"?\.)?"?(\w+)"?',
    ]

    for pattern in patterns:
        for match in re.finditer(pattern, sql, re.IGNORECASE):
            groups = [g for g in match.groups() if g]
            if groups:
                # Use the last group (object name, not schema)
                objects.add(groups[-1].lower())

    return objects


