_MAX_ORDER_INDEX_RETRIES = 5


async def create_migration(
    db: AsyncSession,
    connection_id: UUID,
    workspace_id: UUID,
    label: str,
    up_sql: str,
    down_sql: str | None = None,
    generated_by: str = "manual",
    backup_id: UUID | None = None,
    db_url: str | None = None,
    skip_validation: bool = False,
    skip_conflict_check: bool = False,
) -> MigrationRecord:
    if generated_by not in GENERATED_BY_VALUES:
        raise ValueError(
            f"Unknown generated_by value '{generated_by}'. "
            f"Must be one of: {', '.join(GENERATED_BY_VALUES)}"
        )

    # Permission guard
    if generated_by != "restore" and not skip_conflict_check:
        try:
            await assert_connection_writable(
                db, connection_id,
                operation=f"Creating migration '{label}'"
            )
        except PermissionError:
            raise  

    # SQL validation
    syntax_valid: bool | None = None
    validation_error: str | None = None

    if db_url and not skip_validation and generated_by != "restore":
        # FIXED: Pass connection_id here
        syntax_valid, validation_error = await validate_migration_sql(connection_id, up_sql, db_url)
        if not syntax_valid:
            logger.warning(
                f"Migration SQL validation failed for '{label}': {validation_error}"
            )

    # Conflict detection
    conflict_report: ConflictReport | None = None
    has_conflict = False

    if not skip_conflict_check and generated_by != "restore":
        conflict_report = await detect_conflicts(db, connection_id, up_sql, label)
        has_conflict = conflict_report.has_conflicts
        if has_conflict:
            logger.warning(
                f"Migration '{label}' created with {len(conflict_report.conflicts)} "
                f"conflict(s): {conflict_report.summary}"
            )

    from sqlalchemy.exc import IntegrityError
    import asyncio

    for attempt in range(1, _MAX_ORDER_INDEX_RETRIES + 1):
        try:
            record = await _create_migration_attempt(
                db=db,
                connection_id=connection_id,
                workspace_id=workspace_id,
                label=label,
                up_sql=up_sql,
                down_sql=down_sql,
                generated_by=generated_by,
                backup_id=backup_id,
                syntax_valid=syntax_valid,
                validation_error=validation_error,
                has_conflict=has_conflict,
                conflict_report=conflict_report,
            )
            return record
        except IntegrityError as exc:
            await db.rollback()
            if "uq_migration_connection_order_index" not in str(exc) or attempt == _MAX_ORDER_INDEX_RETRIES:
                raise
            wait = 0.05 * attempt
            logger.warning(
                f"order_index collision on attempt {attempt}/{_MAX_ORDER_INDEX_RETRIES} "
                f"for connection {connection_id} — retrying in {wait:.2f}s"
            )
            await asyncio.sleep(wait)

    raise RuntimeError("create_migration: exhausted retries")


async def _create_migration_attempt(
    db: AsyncSession,
    connection_id: UUID,
    workspace_id: UUID,
    label: str,
    up_sql: str,
    down_sql: str | None,
    generated_by: str,
    backup_id: UUID | None,
    syntax_valid: bool | None,
    validation_error: str | None,
    has_conflict: bool,
    conflict_report: "ConflictReport | None",
) -> MigrationRecord:
    """Single attempt at creating a migration record with SELECT FOR UPDATE locking."""
    lock_result = await db.execute(
        select(MigrationRecord)
        .where(MigrationRecord.connection_id == connection_id)
        .order_by(MigrationRecord.order_index.desc())
        .limit(1)
        .with_for_update()
    )
    last = lock_result.scalar_one_or_none()
    next_order = (last.order_index + 1) if last else 1

    meta: dict = {}
    if validation_error:
        meta["validation_error"] = validation_error
    if conflict_report and conflict_report.has_conflicts:
        meta["conflict_report"] = conflict_report.to_dict()

    record = MigrationRecord(
        connection_id=connection_id,
        workspace_id=workspace_id,
        version=_generate_version(),
        label=label,
        checksum=_checksum(up_sql),
        up_sql=up_sql.strip(),
        down_sql=down_sql.strip() if down_sql else None,
        applied=False,
        order_index=next_order,
        generated_by=generated_by,
        backup_id=backup_id,
        syntax_valid=syntax_valid,
        has_conflict=has_conflict,
        meta=meta,
    )
    db.add(record)
    await db.commit()
    await db.refresh(record)

    status_parts = []
    if syntax_valid is True:
        status_parts.append("syntax_valid=True")
    elif syntax_valid is False:
        status_parts.append("syntax_valid=False")
    else:
        status_parts.append("validation_skipped")
    if has_conflict:
        status_parts.append("has_conflict=True")

    logger.info(
        f"Migration created: {record.version} ({label}) "
        f"order={next_order} {' '.join(status_parts)}"
    )
    return record


async def apply_migration(
    pg_conn: asyncpg.Connection,
    db: AsyncSession,
    migration_id: UUID,
    force_apply: bool = False,
) -> MigrationRecord:
    """
    Apply a single migration to the target database.

    Blocked when:
    - syntax_valid is False (SQL failed pre-storage validation)
    - has_conflict is True AND force_apply is False

    force_apply=True allows applying a conflicted migration after the
    developer has manually reviewed and accepted the risk.
    """
    record = await _get_migration_or_raise(db, migration_id)

    if record.applied:
        raise ValueError(f"Migration {record.version} is already applied.")

    if record.syntax_valid is False:
        validation_error = record.meta.get("validation_error", "unknown error")
        raise ValueError(
            f"Migration {record.version} failed syntax validation before storage "
            f"and cannot be applied: {validation_error}. "
            "Delete this migration, fix the SQL, and create a new one."
        )

    if record.has_conflict and not force_apply:
        conflict_summary = record.meta.get("conflict_report", {}).get(
            "summary", "conflicts detected"
        )
        raise ValueError(
            f"Migration {record.version} has unresolved conflicts: {conflict_summary}. "
            "Resolve conflicts first (POST /migrations/{connection_id}/resolve) "
            "or re-apply with force_apply=true to override."
        )

    try:
        # ---------------------------------------------------------------------
        # UI CONSIDERATION: The frontend MUST expect a maximum response time 
        # of ~35 seconds for this endpoint. If the DB operation takes longer, 
        # the backend will aggressively kill it and return a 400 error. 
        # The UI should display: "Migration aborted: Operation too heavy. 
        # Consider batching or adding CONCURRENTLY to indexes."
        # ---------------------------------------------------------------------
        async with pg_conn.transaction():
            # 1. Enforce aggressive fast-fail on lock acquisition (2 seconds)
            # If another user is querying the table, don't hang the pool waiting.
            await pg_conn.execute("SET LOCAL lock_timeout = '2000';")
            
            # 2. Enforce engine-level circuit breaker for execution length (30 seconds)
            await pg_conn.execute("SET LOCAL statement_timeout = '30000';")
            
            await pg_conn.execute(record.up_sql)

        record.applied = True
        record.applied_at = datetime.now(timezone.utc)
        record.error = None
        record.has_conflict = False  # Clear conflict flag after successful apply
        await db.commit()
        await db.refresh(record)
        logger.info(f"Migration applied: {record.version}")
        return record

    except asyncpg.InsufficientPrivilegeError as exc:
        record.error = (
            f"Insufficient privileges: {exc.args[0]}. "
            "The connected role does not have permission to run this DDL."
        )
        await db.commit()
        logger.error(f"Migration {record.version} failed (privileges): {exc}")
        raise
    except Exception as exc:
        error_msg = str(exc)
        
        # Translate physics violations into clear feedback
        if getattr(exc, "sqlstate", None) == '55P03':  # lock_not_available
            error_msg = "Lock acquisition timed out (2s limit). The table is locked by an active transaction."
        elif isinstance(exc, asyncpg.QueryCanceledError) or "timeout" in error_msg.lower():
            error_msg = "Migration timed out (30s limit). DDL must be optimized or batched."
            
        record.error = error_msg
        await db.commit()
        logger.error(f"Migration {record.version} failed: {error_msg}")
        raise ValueError(error_msg)


async def rollback_migration(
    pg_conn: asyncpg.Connection,
    db: AsyncSession,
    migration_id: UUID,
) -> MigrationRecord:
    record = await _get_migration_or_raise(db, migration_id)

    if not record.applied:
        raise ValueError(f"Migration {record.version} is not applied.")
    if not record.down_sql:
        raise ValueError(
            f"Migration {record.version} has no rollback SQL. "
            "Manual rollback required."
        )
    if record.generated_by == "restore":
        raise ValueError(
            f"Migration {record.version} is an informational restore record "
            "with no executable SQL. It cannot be rolled back."
        )

    try:
        async with pg_conn.transaction():
            await pg_conn.execute(record.down_sql)

        record.applied = False
        record.rolled_back_at = datetime.now(timezone.utc)
        record.error = None
        await db.commit()
        await db.refresh(record)
        logger.info(f"Migration rolled back: {record.version}")
        return record

    except asyncpg.InsufficientPrivilegeError as exc:
        record.error = (
            f"Insufficient privileges: {exc.args[0]}. "
            "The connected role does not have permission to roll back this migration."
        )
        await db.commit()
        logger.error(f"Rollback of {record.version} failed (privileges): {exc}")
        raise
    except Exception as exc:
        record.error = str(exc)
        await db.commit()
        logger.error(f"Rollback of {record.version} failed: {exc}")
        raise


async def apply_pending(
    pg_conn: asyncpg.Connection,
    db: AsyncSession,
    connection_id: UUID,
    force_apply: bool = False,
) -> list[MigrationRecord]:
    """
    Apply all pending migrations in order_index order.

    If any pending migration has has_conflict=True, apply_pending will
    stop at that migration and raise ValueError unless force_apply=True.
    """
    pending = await list_migrations(db, connection_id, applied=False)

    # Pre-flight conflict check — surface all conflicts before touching the DB
    if not force_apply:
        conflicted = [m for m in pending if m.has_conflict and m.generated_by != "restore"]
        if conflicted:
            versions = ", ".join(m.version for m in conflicted)
            raise ValueError(
                f"{len(conflicted)} pending migration(s) have unresolved conflicts "
                f"({versions}). Resolve conflicts first or use force_apply=true."
            )

    applied = []
    for record in pending:
        if record.generated_by == "restore":
            logger.warning(
                f"Skipping restore migration {record.version} in apply_pending "
                "— it should already be marked applied."
            )
            continue
        result = await apply_migration(pg_conn, db, record.id, force_apply=force_apply)
        applied.append(result)
    return applied


# ---------------------------------------------------------------------------
# Conflict resolution workflow
# ---------------------------------------------------------------------------

async def resolve_conflict(
    db: AsyncSession,
    migration_id: UUID,
    strategy: str,
    target_migration_id: UUID | None = None,
) -> MigrationRecord:
    """
    Resolve a conflict on a pending migration.

    Strategies
    ----------
    "accept"       — Clear the conflict flag, allow apply to proceed.
                     Use this after manually reviewing and confirming the
                     SQL is safe to run after the blocking migrations.

    "rebase"       — Move this migration to run AFTER target_migration_id
                     by bumping its order_index. Requires target_migration_id.

    "skip"         — Mark this migration as applied without executing its SQL.
                     Use only when you are certain the schema change was
                     already applied by another means.

    "squash"       — Merge this migration's SQL into target_migration_id's SQL
                     and delete this record. Requires target_migration_id.
                     target_migration_id must also be pending.
    """
    if strategy not in ("accept", "rebase", "skip", "squash"):
        raise ValueError(
            f"Unknown resolution strategy '{strategy}'. "
            "Must be: accept, rebase, skip, squash."
        )

    record = await _get_migration_or_raise(db, migration_id)

    if record.applied:
        raise ValueError(
            f"Migration {record.version} is already applied and cannot be resolved."
        )

    if strategy == "accept":
        record.has_conflict = False
        meta = dict(record.meta)
        meta.pop("conflict_report", None)
        meta["conflict_resolved"] = {
            "strategy": "accept",
            "resolved_at": datetime.now(timezone.utc).isoformat(),
        }
        record.meta = meta
        await db.commit()
        await db.refresh(record)
        logger.info(f"Conflict accepted for migration {record.version}")
        return record

    if strategy == "rebase":
        if not target_migration_id:
            raise ValueError("'rebase' strategy requires target_migration_id.")

        target = await _get_migration_or_raise(db, target_migration_id)
        if target.applied:
            raise ValueError(
                f"Target migration {target.version} is already applied. "
                "Rebase to a pending migration."
            )

        # Place this migration immediately after the target
        new_order = target.order_index + 1

        # Shift all migrations after the target up by 1 to make room
        await db.execute(
            update(MigrationRecord)
            .where(
                MigrationRecord.connection_id == record.connection_id,
                MigrationRecord.order_index >= new_order,
                MigrationRecord.id != record.id,
                MigrationRecord.applied == False,  # noqa: E712
            )
            .values(order_index=MigrationRecord.order_index + 1)
        )

        record.order_index = new_order
        record.has_conflict = False
        meta = dict(record.meta)
        meta["conflict_resolved"] = {
            "strategy": "rebase",
            "after_migration": str(target_migration_id),
            "resolved_at": datetime.now(timezone.utc).isoformat(),
        }
        record.meta = meta
        await db.commit()
        await db.refresh(record)
        logger.info(
            f"Migration {record.version} rebased to order_index={new_order}"
        )
        return record

    if strategy == "skip":
        record.applied = True
        record.applied_at = datetime.now(timezone.utc)
        record.has_conflict = False
        meta = dict(record.meta)
        meta["conflict_resolved"] = {
            "strategy": "skip",
            "note": "Marked applied without executing SQL.",
            "resolved_at": datetime.now(timezone.utc).isoformat(),
        }
        record.meta = meta
        await db.commit()
        await db.refresh(record)
        logger.info(f"Migration {record.version} skipped (marked applied without execution)")
        return record

    if strategy == "squash":
        if not target_migration_id:
            raise ValueError("'squash' strategy requires target_migration_id.")

        target = await _get_migration_or_raise(db, target_migration_id)
        if target.applied:
            raise ValueError(
                f"Target migration {target.version} is already applied. "
                "Cannot squash into an applied migration."
            )
        if record.id == target.id:
            raise ValueError("Cannot squash a migration into itself.")

        # Merge this migration's SQL into the target
        separator = (
            f"\n\n-- Squashed from migration {record.version} "
            f"({record.label}) --\n"
        )
        target.up_sql = target.up_sql.rstrip() + separator + record.up_sql
        target.checksum = _checksum(target.up_sql)
        target.label = f"{target.label} + {record.label}"[:255]
        target.has_conflict = False

        meta = dict(target.meta)
        meta["squashed_migrations"] = meta.get("squashed_migrations", []) + [
            {
                "version": record.version,
                "label": record.label,
                "squashed_at": datetime.now(timezone.utc).isoformat(),
            }
        ]
        target.meta = meta

        # Delete the squashed record
        await db.delete(record)
        await db.commit()
        await db.refresh(target)
        logger.info(
            f"Migration {record.version} squashed into {target.version}"
        )
        return target

    # Unreachable but keeps type checker happy
    raise ValueError(f"Unhandled strategy: {strategy}")


