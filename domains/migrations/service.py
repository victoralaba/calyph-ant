class MigrationRecord(Base):
    __tablename__ = "migration_records"

    __table_args__ = (
        UniqueConstraint(
            "connection_id",
            "order_index",
            name="uq_migration_connection_order_index",
        ),
    )

    id: Mapped[UUID] = mapped_column(PG_UUID(as_uuid=True), primary_key=True, default=uuid4)
    connection_id: Mapped[UUID] = mapped_column(
        PG_UUID(as_uuid=True),
        ForeignKey("connections.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )
    workspace_id: Mapped[UUID] = mapped_column(PG_UUID(as_uuid=True), nullable=False, index=True)

    # Migration identity
    version: Mapped[str] = mapped_column(String(40), nullable=False)
    label: Mapped[str] = mapped_column(String(255), nullable=False)
    checksum: Mapped[str] = mapped_column(String(64), nullable=False)

    # SQL
    up_sql: Mapped[str] = mapped_column(Text, nullable=False)
    down_sql: Mapped[str | None] = mapped_column(Text, nullable=True)

    # State
    applied: Mapped[bool] = mapped_column(Boolean, default=False, nullable=False)
    applied_at: Mapped[datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    rolled_back_at: Mapped[datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    error: Mapped[str | None] = mapped_column(Text, nullable=True)

    # Ordering
    order_index: Mapped[int] = mapped_column(Integer, nullable=False, default=0)

    # Provenance — backup that triggered this migration (if any)
    backup_id: Mapped[UUID | None] = mapped_column(
        PG_UUID(as_uuid=True),
        ForeignKey("backup_records.id", ondelete="SET NULL"),
        nullable=True,
        index=True,
        comment=(
            "BackupRecord that triggered this migration via restore. "
            "NULL for all other generated_by values."
        ),
    )

    # Provenance string
    generated_by: Mapped[str] = mapped_column(
        String(40), default="manual", nullable=False
    )

    # Whether up_sql passed dry-run validation against the target DB
    syntax_valid: Mapped[bool | None] = mapped_column(
        Boolean, nullable=True, default=None,
        comment="Result of dry-run syntax check before storage. None = skipped.",
    )

    # Conflict tracking — set when this migration was created while conflicts exist
    has_conflict: Mapped[bool] = mapped_column(
        Boolean, default=False, nullable=False,
        comment="True when this migration was created with unresolved conflicts.",
    )

    meta: Mapped[dict] = mapped_column(JSONB, default=dict, nullable=False)

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False,
        default=lambda: datetime.now(timezone.utc)
    )


